class CartDataset(Dataset):
    """
    PyTorch Dataset for cart add-to-cart prediction.
    
    Handles:
    - Loading and preprocessing cart event data
    - Feature extraction from multiple sources
    - Index mapping for item embeddings
    
    Memory-efficient: stores only essential data, computes features on-the-fly.
    """
    
    def __init__(
        self,
        cart_events_df: pd.DataFrame,
        item_id_to_idx: Dict[str, int],
        user_features_df: Optional[pd.DataFrame] = None,
        restaurant_features_df: Optional[pd.DataFrame] = None,
        menu_items_df: Optional[pd.DataFrame] = None
    ):
        """
        Initialize dataset from preprocessed DataFrames.
        
        Args:
            cart_events_df: DataFrame with cart event columns
            item_id_to_idx: Mapping from item_id to embedding index
            user_features_df: Pre-computed user features (optional)
            restaurant_features_df: Pre-computed restaurant features (optional)
            menu_items_df: Menu items for price lookup (optional)
        """
        self.item_id_to_idx = item_id_to_idx
        self.data = []
        
        # Default user features (normalized values, should be ~0 mean, ~1 std)
        self.default_user_features = [0.0, 0.0, 0.0, 0.5, 0.0, 0.1, 0.15]
        
        # Default restaurant features (normalized values)
        self.default_restaurant_features = [0.25, 0.0, 0.0, 0.5, 0.0]
        
        # Build user feature lookup using normalized columns
        self.user_features_lookup: Dict[str, List[float]] = {}
        if user_features_df is not None:
            # Require normalized columns (no fallback)
            if "recency_days_norm" not in user_features_df.columns:
                raise ValueError("User features must have _norm columns. Run feature_builder.py first.")
            
            for _, row in user_features_df.iterrows():
                self.user_features_lookup[row["user_id"]] = [
                    float(row.get("recency_days_norm", 0.0)),
                    float(row.get("frequency_norm", 0.0)),
                    float(row.get("monetary_avg_norm", 0.0)),
                    float(row.get("cuisine_entropy_norm", 0.5)),
                    float(row.get("avg_cart_size_norm", 0.0)),
                    float(row.get("dessert_ratio_norm", 0.1)),
                    float(row.get("beverage_ratio_norm", 0.15)),
                ]
        
        # Build restaurant feature lookup using normalized columns
        self.restaurant_features_lookup: Dict[str, List[float]] = {}
        if restaurant_features_df is not None:
            # Require normalized columns (no fallback)
            if "smoothed_rating_norm" not in restaurant_features_df.columns:
                raise ValueError("Restaurant features must have _norm columns. Run feature_builder.py first.")
            
            for _, row in restaurant_features_df.iterrows():
                self.restaurant_features_lookup[row["restaurant_id"]] = [
                    float(row.get("smoothed_rating_norm", 0.25)),
                    float(row.get("delivery_votes_norm", 0.0)),
                    float(row.get("avg_item_price_norm", 0.0)),
                    float(row.get("price_band_index_norm", 0.5)),
                    float(row.get("menu_size_norm", 0.0)),
                ]
        
        # Build item price lookup and category lookup
        self.item_prices: Dict[str, float] = {}
        self.item_categories: Dict[str, int] = {}  # item_id -> category_index (0=main, 1=starter, 2=dessert, 3=beverage, 4=side)
        if menu_items_df is not None:
            for _, row in menu_items_df.iterrows():
                self.item_prices[row["item_id"]] = float(row.get("price", 100))
                # Map category string to index
                cat = str(row.get("item_category", "other")).lower()
                cat_map = {"main": 0, "starter": 1, "dessert": 2, "beverage": 3, "side": 4, "snack": 5, "combo": 6, "other": 7}
                self.item_categories[row["item_id"]] = cat_map.get(cat, 7)
        
        # Process cart events
        self._process_events(cart_events_df)
        
        logger.info(f"CartDataset initialized with {len(self.data):,} samples")
    
    def _process_events(self, df: pd.DataFrame):
        """Process cart events into training samples."""
        for _, row in df.iterrows():
            # Parse cart items
            cart_items_str = str(row.get("cart_items", ""))
            if cart_items_str and cart_items_str != "nan":
                cart_item_ids = [x.strip() for x in cart_items_str.split("|") if x.strip()]
            else:
                cart_item_ids = []
            
            # Convert to embedding indices (shift by +1, 0 is padding)
            cart_indices = []
            for item_id in cart_item_ids[:MAX_CART_SIZE]:  # Truncate to max size
                idx = self.item_id_to_idx.get(item_id, -1)
                if idx >= 0:
                    cart_indices.append(idx + 1)  # +1 for padding index
            
            # Get candidate item index
            candidate_id = row.get("candidate_item", "")
            candidate_idx = self.item_id_to_idx.get(candidate_id, -1)
            if candidate_idx < 0:
                continue  # Skip if candidate not in embeddings
            candidate_idx += 1  # +1 for padding
            
            # Get label
            label = int(row.get("label", 0))
            
            # User features
            user_id = row.get("user_id", "")
            user_features = self.user_features_lookup.get(
                user_id, self.default_user_features.copy()
            )
            
            # Restaurant features
            restaurant_id = row.get("restaurant_id", "")
            restaurant_features = self.restaurant_features_lookup.get(
                restaurant_id, self.default_restaurant_features.copy()
            )
            
            # Cart dynamic features
            cart_total = float(row.get("cart_total", 0))
            cart_size = len(cart_indices)
            
            # Calculate avg item price in cart
            if cart_item_ids:
                prices = [self.item_prices.get(item_id, 100) for item_id in cart_item_ids]
                avg_cart_price = np.mean(prices)
            else:
                avg_cart_price = 0.0
            
            # Behavioral features computed from cart_categories
            cart_categories_str = str(row.get("cart_categories", ""))
            cart_categories = set(cart_categories_str.split("|")) if cart_categories_str and cart_categories_str != "nan" else set()
            
            missing_beverage = 1.0 if "beverage" not in cart_categories else 0.0
            missing_dessert = 1.0 if "dessert" not in cart_categories else 0.0
            
            # Heavy meal: 2+ mains or main + side
            main_count = cart_categories_str.count("main") if cart_categories_str else 0
            side_count = cart_categories_str.count("side") if cart_categories_str else 0
            heavy_meal = 1.0 if (main_count >= 2 or (main_count >= 1 and side_count >= 1)) else 0.0
            
            cart_dynamic_features = [
                cart_total / 1000.0,       # Normalize cart total
                cart_size / MAX_CART_SIZE,  # Normalize cart size
                avg_cart_price / 500.0,     # Normalize avg price
                missing_beverage,           # Already 0/1
                missing_dessert,            # Already 0/1
                heavy_meal                  # Already 0/1
            ]
            
            # Context features
            hour = int(row.get("hour", 12))
            meal_type = str(row.get("meal_type", "lunch"))
            user_type = str(row.get("user_type", "moderate"))
            
            context_features = [
                hour / 24.0,  # Normalized hour
                float(MEAL_TYPE_MAP.get(meal_type, 1)),
                float(USER_TYPE_MAP.get(user_type, 1))
            ]
            
            # Candidate category one-hot features (main, dessert, beverage, side)
            candidate_category_idx = self.item_categories.get(candidate_id, 7)
            is_main = 1.0 if candidate_category_idx == 0 else 0.0
            is_dessert = 1.0 if candidate_category_idx == 2 else 0.0
            is_beverage = 1.0 if candidate_category_idx == 3 else 0.0
            is_side = 1.0 if candidate_category_idx == 4 else 0.0
            
            context_features.extend([is_main, is_dessert, is_beverage, is_side])
            
            # Timestamp for sorting
            timestamp = str(row.get("timestamp", ""))
            
            # Create sample
            sample = TrainingSample(
                cart_indices=cart_indices,
                candidate_index=candidate_idx,
                label=label,
                user_features=user_features,
                restaurant_features=restaurant_features,
                cart_dynamic_features=cart_dynamic_features,
                context_features=context_features,
                timestamp=timestamp
            )
            
            self.data.append(sample)
    
    def __len__(self) -> int:
        return len(self.data)
    
    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        """
        Get a single training sample.
        
        Returns dict with all required tensors (except batching/padding).
        """
        sample = self.data[idx]
        
        return {
            "cart_indices": sample.cart_indices,  # Variable length list
            "candidate_index": sample.candidate_index,
            "label": sample.label,
            "user_features": torch.tensor(sample.user_features, dtype=torch.float32),
            "restaurant_features": torch.tensor(sample.restaurant_features, dtype=torch.float32),
            "cart_dynamic_features": torch.tensor(sample.cart_dynamic_features, dtype=torch.float32),
            "context_features": torch.tensor(sample.context_features, dtype=torch.float32),
        }


