def load_item_mapping(mapping_path: Path) -> Dict[str, int]:
    """
    Load item ID to embedding index mapping.
    
    The mapping file contains {'item_to_idx': {...}, 'idx_to_item': {...}, 'metadata': {...}}.
    """
    with open(mapping_path, "rb") as f:
        data = pickle.load(f)
    
    # Handle different formats
    if isinstance(data, dict) and "item_to_idx" in data:
        # New format with explicit item_to_idx mapping
        item_to_idx = data["item_to_idx"]
    elif isinstance(data, list):
        # Old format: [idx_to_item_dict, metadata]
        idx_to_item = data[0]
        item_to_idx = {item_id: idx for idx, item_id in idx_to_item.items()}
    else:
        # Assume it's a direct idx_to_item dict
        idx_to_item = data
        item_to_idx = {item_id: idx for idx, item_id in idx_to_item.items()}
    
    logger.info(f"Loaded item mapping: {len(item_to_idx):,} items")
    return item_to_idx


def load_data(
    sample_size: int = DEFAULT_SAMPLE_SIZE,
    val_ratio: float = DEFAULT_VAL_RATIO
) -> Tuple[CartDataset, CartDataset, Dict[str, int]]:
    """
    Load and preprocess training data.
    
    Args:
        sample_size: Number of rows to load (for memory efficiency)
        val_ratio: Fraction of data for validation (time-based split)
    
    Returns:
        train_dataset, val_dataset, item_to_idx mapping
    """
    logger.info("=" * 60)
    logger.info("Loading Training Data")
    logger.info("=" * 60)
    
    # Load item mapping
    item_to_idx = load_item_mapping(ITEM_MAPPING_PATH)
    
    # Load cart events (sample for memory efficiency)
    logger.info(f"Loading cart events (sample_size={sample_size:,})...")
    
    # Read in chunks to get timestamps for time-based split
    chunks = []
    rows_read = 0
    
    for chunk in pd.read_csv(CART_EVENTS_PATH, chunksize=500_000):
        chunks.append(chunk)
        rows_read += len(chunk)
        if rows_read >= sample_size:
            break
        if rows_read % 1_000_000 == 0:
            logger.info(f"  Read {rows_read:,} rows...")
    
    df = pd.concat(chunks, ignore_index=True)
    df = df.head(sample_size)  # Ensure exact size
    
    logger.info(f"Loaded {len(df):,} cart events")
    
    # Compute label distribution
    label_counts = df["label"].value_counts()
    logger.info(f"Label distribution: {label_counts.to_dict()}")
    
    # Sort by timestamp for time-based split
    df["timestamp"] = pd.to_datetime(df["timestamp"], errors="coerce")
    df = df.sort_values("timestamp").reset_index(drop=True)
    
    # Time-based split
    split_idx = int(len(df) * (1 - val_ratio))
    train_df = df.iloc[:split_idx].copy()
    val_df = df.iloc[split_idx:].copy()
    
    # DEBUG: Print label means
    train_label_mean = train_df["label"].mean()
    val_label_mean = val_df["label"].mean()
    logger.info(f"[DEBUG] Train label mean: {train_label_mean:.4f}")
    logger.info(f"[DEBUG] Val label mean: {val_label_mean:.4f}")
    
    logger.info(f"Train/Val split: {len(train_df):,} / {len(val_df):,}")
    
    # Load auxiliary data - require normalized parquet files (no fallback)
    logger.info("Loading auxiliary data...")
    
    users_df = None
    if USER_FEATURES_PATH.exists():
        users_df = pd.read_parquet(USER_FEATURES_PATH)
        logger.info(f"  Users (normalized parquet): {len(users_df):,}")
        norm_cols = [c for c in users_df.columns if c.endswith("_norm")]
        logger.info(f"    Normalized columns: {norm_cols}")
        if not norm_cols:
            raise ValueError(f"User parquet must have _norm columns. Regenerate with feature_builder.py")
    else:
        raise FileNotFoundError(f"User features parquet not found: {USER_FEATURES_PATH}")
    
    restaurants_df = None
    if RESTAURANT_FEATURES_PATH.exists():
        restaurants_df = pd.read_parquet(RESTAURANT_FEATURES_PATH)
        logger.info(f"  Restaurants (normalized parquet): {len(restaurants_df):,}")
        norm_cols = [c for c in restaurants_df.columns if c.endswith("_norm")]
        logger.info(f"    Normalized columns: {norm_cols}")
        if not norm_cols:
            raise ValueError(f"Restaurant parquet must have _norm columns. Regenerate with feature_builder.py")
    else:
        raise FileNotFoundError(f"Restaurant features parquet not found: {RESTAURANT_FEATURES_PATH}")
    
    menu_items_df = None
    if MENU_ITEMS_PATH.exists():
        menu_items_df = pd.read_csv(MENU_ITEMS_PATH)
        logger.info(f"  Menu items: {len(menu_items_df):,}")
    
    # Build datasets
    logger.info("Building datasets...")
    
    train_dataset = CartDataset(
        train_df, item_to_idx,
        user_features_df=users_df,
        restaurant_features_df=restaurants_df,
        menu_items_df=menu_items_df
    )
    
    val_dataset = CartDataset(
        val_df, item_to_idx,
        user_features_df=users_df,
        restaurant_features_df=restaurants_df,
        menu_items_df=menu_items_df
    )
    
    # Cleanup
    del df, train_df, val_df, chunks
    gc.collect()
    
    return train_dataset, val_dataset, item_to_idx


# -----------------------------------------------------------------------------
# Main Training Loop
# -----------------------------------------------------------------------------

def train(
    batch_size: int = DEFAULT_BATCH_SIZE,
    epochs: int = DEFAULT_EPOCHS,
    lr: float = DEFAULT_LR,
    sample_size: int = DEFAULT_SAMPLE_SIZE,
    val_ratio: float = DEFAULT_VAL_RATIO,
    checkpoint_freq: int = 2,
    device: str = "cpu",
    disable_transformer: bool = False
):
    """
    Main training function.
    
    Args:
        batch_size: Training batch size
        epochs: Number of training epochs
        lr: Learning rate
        sample_size: Number of training samples to use
        val_ratio: Validation set ratio (time-based split)
        checkpoint_freq: Save checkpoint every N epochs
        device: Training device ('cpu' or 'cuda')
        disable_transformer: If True, use mean pooling instead of transformer
    """
    logger.info("=" * 60)
    logger.info("Cart Add-to-Cart Model Training")
    logger.info("=" * 60)
    logger.info(f"Device: {device}")
    logger.info(f"Batch size: {batch_size:,}")
    logger.info(f"Epochs: {epochs}")
    logger.info(f"Learning rate: {lr}")
    logger.info(f"Sample size: {sample_size:,}")
    logger.info(f"Transformer: {'DISABLED' if disable_transformer else 'enabled'}")
    
    # Create directories
    CHECKPOINTS_DIR.mkdir(parents=True, exist_ok=True)
    LOGS_DIR.mkdir(parents=True, exist_ok=True)
    
    # Load data
    train_dataset, val_dataset, item_to_idx = load_data(sample_size, val_ratio)
    
    # Create data loaders
    train_loader = DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        collate_fn=collate_fn,
        num_workers=0,  # CPU training
        pin_memory=False
    )
    
    val_loader = DataLoader(
        val_dataset,
        batch_size=batch_size * 2,  # Larger batch for eval
        shuffle=False,
        collate_fn=collate_fn,
        num_workers=0
    )
    
    logger.info(f"Train batches: {len(train_loader):,}")
    logger.info(f"Val batches: {len(val_loader):,}")
    
    # Compute class weights for imbalanced data
    all_labels = torch.tensor([s.label for s in train_dataset.data])
    pos_weight = compute_class_weights(all_labels)
    
    # Compute prior probability for bias initialization
    prior_prob = all_labels.float().mean().item()
    logger.info(f"[DEBUG] Prior probability (for bias init): {prior_prob:.4f}")
    
    # Initialize model
    logger.info("Initializing model...")
    model = CartAddToCartModel(
        item_embeddings_path=EMBEDDINGS_PATH,
        user_feature_dim=7,
        restaurant_feature_dim=5,
        cart_dynamic_feature_dim=6,  # Now includes missing_beverage, missing_dessert, heavy_meal
        context_feature_dim=7,  # hour, meal_type, user_type + 4 candidate category one-hot features
        freeze_embeddings=True,
        disable_transformer=disable_transformer,
        prior_prob=prior_prob
    )
    model.to(device)
    
    # Loss function with class weighting
    criterion = nn.BCEWithLogitsLoss(pos_weight=pos_weight.to(device))
    
    # Optimizer
    optimizer = AdamW(
        model.parameters(),
        lr=lr,
        weight_decay=0.01,
        betas=(0.9, 0.999)
    )
    
    # Learning rate scheduler
    scheduler = ReduceLROnPlateau(
        optimizer,
        mode="max",  # Maximize AUC
        factor=0.5,
        patience=2
    )
    
    # Training config for checkpointing
    config = {
        "batch_size": batch_size,
        "epochs": epochs,
        "lr": lr,
        "sample_size": sample_size,
        "val_ratio": val_ratio,
        "embeddings_path": str(EMBEDDINGS_PATH),
        "user_feature_dim": 7,
        "restaurant_feature_dim": 5,
        "cart_dynamic_feature_dim": 6,
        "context_feature_dim": 7,  # hour, meal_type, user_type + 4 candidate category features
        "embedding_dim": 64,
    }
    
    # Training history
    history = {
        "train_loss": [],
        "val_loss": [],
        "val_auc": [],
        "val_pr_auc": [],
        "lr": []
    }
    
    best_auc = 0.0
    best_epoch = 0
    best_pr_auc = 0.0
    patience_counter = 0
    early_stop_patience = 2  # Stop if no improvement for 2 epochs
    
    logger.info("\nStarting training...")
    logger.info("-" * 60)
    logger.info(f"Early stopping: patience={early_stop_patience} epochs on val AUC")
    
    training_start = time.time()
    
    for epoch in range(1, epochs + 1):
        epoch_start = time.time()
        
        # Train
        train_loss, train_preds, train_labels = train_epoch(
            model, train_loader, optimizer, criterion, device
        )
        
        # Evaluate
        val_loss, val_auc, val_pr_auc, val_preds, val_labels = evaluate(
            model, val_loader, criterion, device
        )
        
        # Update scheduler
        scheduler.step(val_auc)
        
        # Track history
        current_lr = optimizer.param_groups[0]["lr"]
        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["val_auc"].append(val_auc)
        history["val_pr_auc"].append(val_pr_auc)
        history["lr"].append(current_lr)
        
        epoch_time = time.time() - epoch_start
        
        # Logging
        logger.info(
            f"Epoch {epoch:2d}/{epochs} | "
            f"Train Loss: {train_loss:.4f} | "
            f"Val Loss: {val_loss:.4f} | "
            f"AUC: {val_auc:.4f} | "
            f"PR-AUC: {val_pr_auc:.4f} | "
            f"LR: {current_lr:.2e} | "
            f"Time: {epoch_time:.1f}s"
        )
        
        # Best model checkpoint
        if val_auc > best_auc:
            best_auc = val_auc
            best_pr_auc = val_pr_auc
            best_epoch = epoch
            patience_counter = 0  # Reset patience counter
            best_checkpoint_path = CHECKPOINTS_DIR / "best_model_final.pt"
            save_checkpoint(
                model, optimizer, epoch,
                {"val_auc": val_auc, "val_pr_auc": val_pr_auc, "val_loss": val_loss},
                config, best_checkpoint_path
            )
            logger.info(f"  ★ New best model (AUC: {val_auc:.4f}, PR-AUC: {val_pr_auc:.4f})")
        else:
            patience_counter += 1
            logger.info(f"  No improvement ({patience_counter}/{early_stop_patience})")
        
        # Regular checkpoint
        if epoch % checkpoint_freq == 0:
            checkpoint_path = CHECKPOINTS_DIR / f"checkpoint_epoch_{epoch}.pt"
            save_checkpoint(
                model, optimizer, epoch,
                {"val_auc": val_auc, "val_pr_auc": val_pr_auc, "val_loss": val_loss},
                config, checkpoint_path
            )
        
        # Early stopping check
        if patience_counter >= early_stop_patience:
            logger.info(f"\n⚠️  Early stopping triggered at epoch {epoch}")
            logger.info(f"   No improvement in val AUC for {early_stop_patience} epochs")
            break
    
    total_time = time.time() - training_start
    
    # Training summary
    logger.info("\n" + "=" * 60)
    logger.info("TRAINING COMPLETE - FINAL METRICS SUMMARY")
    logger.info("=" * 60)
    logger.info(f"Total training time: {total_time/60:.1f} minutes ({total_time:.1f}s)")
    logger.info(f"Epochs completed: {len(history['train_loss'])}")
    logger.info(f"Best epoch: {best_epoch}")
    logger.info("-" * 60)
    logger.info("BEST MODEL METRICS:")
    logger.info(f"  ★ Best Val AUC:    {best_auc:.6f}")
    logger.info(f"  ★ Best Val PR-AUC: {best_pr_auc:.6f}")
    logger.info("-" * 60)
    logger.info("FINAL EPOCH METRICS:")
    logger.info(f"  Val Loss:   {history['val_loss'][-1]:.6f}")
    logger.info(f"  Val AUC:    {history['val_auc'][-1]:.6f}")
    logger.info(f"  Val PR-AUC: {history['val_pr_auc'][-1]:.6f}")
    logger.info("-" * 60)
    logger.info("TRAINING LOSS PER EPOCH:")
    for i, loss in enumerate(history['train_loss'], 1):
        logger.info(f"  Epoch {i}: {loss:.6f}")
    logger.info("=" * 60)
    
    # Save final checkpoint
    final_checkpoint_path = CHECKPOINTS_DIR / "final_model.pt"
    save_checkpoint(
        model, optimizer, epochs,
        history, config, final_checkpoint_path
    )
    
    # Save training history
    history_path = LOGS_DIR / "training_history.pkl"
    with open(history_path, "wb") as f:
        pickle.dump(history, f)
    logger.info(f"Training history saved: {history_path}")
    
    return model, history


