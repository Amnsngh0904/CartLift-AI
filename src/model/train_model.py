@dataclass
class TrainingSample:
    """Single training sample with all required features."""
    cart_indices: List[int]      # Item embedding indices in cart
    candidate_index: int         # Candidate item embedding index
    label: int                   # 1 = added, 0 = not added
    user_features: List[float]   # User feature vector
