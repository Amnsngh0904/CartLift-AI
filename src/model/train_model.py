def compute_class_weights(labels: torch.Tensor) -> torch.Tensor:
    """
    Compute class weights for imbalanced binary classification.
    
    Uses inverse frequency weighting to handle class imbalance.
    
    Args:
        labels: Binary labels tensor
    
    Returns:
        pos_weight for BCEWithLogitsLoss (weight for positive class)
    """
    num_pos = labels.sum().item()
    num_neg = len(labels) - num_pos
    
    if num_pos == 0 or num_neg == 0:
        return torch.tensor(1.0)
    
    # pos_weight = num_neg / num_pos
    pos_weight = num_neg / num_pos
    
    logger.info(f"Class distribution: pos={num_pos:,}, neg={num_neg:,}")
    logger.info(f"Positive class weight: {pos_weight:.3f}")
    logger.info(f"[DEBUG] pos_weight = {pos_weight:.6f}")
    
    return torch.tensor(pos_weight)


def train_epoch(
    model: CartAddToCartModel,
    dataloader: DataLoader,
    optimizer: torch.optim.Optimizer,
    criterion: nn.Module,
    device: str = "cpu"
) -> Tuple[float, List[float], List[int]]:
    """
    Train for one epoch.
    
    Returns:
        avg_loss: Average loss over batches
        all_preds: All prediction probabilities
        all_labels: All true labels
    """
    model.train()
    total_loss = 0.0
    num_batches = 0
    first_batch_logged = False
    
    all_preds = []
    all_labels = []
    
    for batch in dataloader:
        # Move to device
        cart_indices = batch["cart_indices"].to(device)
        candidate_indices = batch["candidate_indices"].to(device)
        labels = batch["labels"].to(device)
        user_features = batch["user_features"].to(device)
        restaurant_features = batch["restaurant_features"].to(device)
        cart_dynamic_features = batch["cart_dynamic_features"].to(device)
        context_features = batch["context_features"].to(device)
        cart_mask = batch["cart_mask"].to(device)
        
        # Forward pass
        optimizer.zero_grad()
        logits = model(
            cart_indices, candidate_indices,
            user_features, restaurant_features,
            cart_dynamic_features, context_features,
            cart_mask
        )
        
        # Compute loss
        loss = criterion(logits, labels)
        
        # Backward pass
        loss.backward()
        
        # Gradient clipping
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        
        optimizer.step()
        
        # Track metrics
        total_loss += loss.item()
        num_batches += 1
        
        # Store predictions for metrics
        with torch.no_grad():
            preds = torch.sigmoid(logits).squeeze().cpu().numpy()
            labs = labels.squeeze().cpu().numpy()
            
            # DEBUG: Log first batch prediction stats
            if not first_batch_logged:
                pred_mean = preds.mean() if preds.ndim > 0 else preds.item()
                pred_std = preds.std() if preds.ndim > 0 else 0.0
                label_mean = labs.mean() if labs.ndim > 0 else labs.item()
                logger.info(f"[DEBUG] First batch prediction mean: {pred_mean:.4f}, std: {pred_std:.4f}")
                logger.info(f"[DEBUG] First batch label mean: {label_mean:.4f}")
                first_batch_logged = True
            
            if preds.ndim == 0:
                preds = np.array([preds.item()])
                labs = np.array([labs.item()])
            
            all_preds.extend(preds.tolist())
            all_labels.extend(labs.astype(int).tolist())
    
    avg_loss = total_loss / max(num_batches, 1)
    return avg_loss, all_preds, all_labels


@torch.no_grad()
def evaluate(
    model: CartAddToCartModel,
    dataloader: DataLoader,
    criterion: nn.Module,
    device: str = "cpu"
) -> Tuple[float, float, float, List[float], List[int]]:
    """
    Evaluate model on validation set.
    
    Returns:
        avg_loss: Average loss
        auc: ROC-AUC score
        pr_auc: PR-AUC score
        all_preds: Prediction probabilities
        all_labels: True labels
    """
    model.eval()
    total_loss = 0.0
    num_batches = 0
    
    all_preds = []
    all_labels = []
    
    for batch in dataloader:
        cart_indices = batch["cart_indices"].to(device)
        candidate_indices = batch["candidate_indices"].to(device)
        labels = batch["labels"].to(device)
        user_features = batch["user_features"].to(device)
        restaurant_features = batch["restaurant_features"].to(device)
        cart_dynamic_features = batch["cart_dynamic_features"].to(device)
        context_features = batch["context_features"].to(device)
        cart_mask = batch["cart_mask"].to(device)
        
        logits = model(
            cart_indices, candidate_indices,
            user_features, restaurant_features,
            cart_dynamic_features, context_features,
            cart_mask
        )
        
        loss = criterion(logits, labels)
        total_loss += loss.item()
        num_batches += 1
        
        preds = torch.sigmoid(logits).squeeze().cpu().numpy()
        labs = labels.squeeze().cpu().numpy()
        
        if preds.ndim == 0:
            preds = np.array([preds.item()])
            labs = np.array([labs.item()])
        
        all_preds.extend(preds.tolist())
        all_labels.extend(labs.astype(int).tolist())
    
    avg_loss = total_loss / max(num_batches, 1)
    
    # Compute metrics
    try:
        auc = roc_auc_score(all_labels, all_preds)
    except ValueError:
        auc = 0.5  # Only one class present
    
    try:
        pr_auc = average_precision_score(all_labels, all_preds)
    except ValueError:
        pr_auc = 0.0
    
    return avg_loss, auc, pr_auc, all_preds, all_labels


