def main():
    parser = argparse.ArgumentParser(
        description="Train Cart Add-to-Cart Prediction Model"
    )
    
    parser.add_argument(
        "--batch-size", type=int, default=DEFAULT_BATCH_SIZE,
        help=f"Training batch size (default: {DEFAULT_BATCH_SIZE})"
    )
    parser.add_argument(
        "--epochs", type=int, default=DEFAULT_EPOCHS,
        help=f"Number of training epochs (default: {DEFAULT_EPOCHS})"
    )
    parser.add_argument(
        "--lr", type=float, default=DEFAULT_LR,
        help=f"Learning rate (default: {DEFAULT_LR})"
    )
    parser.add_argument(
        "--sample-size", type=int, default=DEFAULT_SAMPLE_SIZE,
        help=f"Number of training samples (default: {DEFAULT_SAMPLE_SIZE:,})"
    )
    parser.add_argument(
        "--val-ratio", type=float, default=DEFAULT_VAL_RATIO,
        help=f"Validation ratio (default: {DEFAULT_VAL_RATIO})"
    )
    parser.add_argument(
        "--checkpoint-freq", type=int, default=2,
        help="Checkpoint every N epochs (default: 2)"
    )
    parser.add_argument(
        "--device", type=str, default="cpu",
        choices=["cpu", "cuda", "mps"],
        help="Training device (default: cpu)"
    )
    parser.add_argument(
        "--disable-transformer", action="store_true",
        help="Disable transformer, use mean pooling instead (for debugging)"
    )
    
    args = parser.parse_args()
    
    # Validate device
    device = args.device
    if device == "cuda" and not torch.cuda.is_available():
        logger.warning("CUDA not available, falling back to CPU")
        device = "cpu"
    if device == "mps" and not torch.backends.mps.is_available():
        logger.warning("MPS not available, falling back to CPU")
        device = "cpu"
    
    # Run training
    train(
        batch_size=args.batch_size,
        epochs=args.epochs,
        lr=args.lr,
        sample_size=args.sample_size,
        val_ratio=args.val_ratio,
        checkpoint_freq=args.checkpoint_freq,
        device=device,
        disable_transformer=args.disable_transformer
    )


if __name__ == "__main__":
    main()
