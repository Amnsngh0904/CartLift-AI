# Collate Function (Dynamic Padding)
# -----------------------------------------------------------------------------

def collate_fn(batch: List[Dict]) -> Dict[str, torch.Tensor]:
    """
    Custom collate function for variable-length cart sequences.
    
    Pads cart sequences to the maximum length in the batch (not global max).
    This is more memory-efficient than padding to a fixed maximum size.
    
    Args:
        batch: List of samples from CartDataset.__getitem__
    
    Returns:
        Batched tensors with padding applied
    """
    batch_size = len(batch)
    
    # Find max cart length in this batch
    cart_lengths = [len(sample["cart_indices"]) for sample in batch]
    max_cart_len = max(cart_lengths) if cart_lengths else 0
    max_cart_len = max(max_cart_len, 1)  # At least 1 to avoid empty tensors
    
    # Initialize padded cart indices (0 is padding index)
    cart_indices = torch.zeros(batch_size, max_cart_len, dtype=torch.long)
    
    # Fill in actual values
    for i, sample in enumerate(batch):
        indices = sample["cart_indices"]
        if indices:
            cart_indices[i, :len(indices)] = torch.tensor(indices, dtype=torch.long)
    
    # Create padding mask (True = padding position)
    cart_lengths_tensor = torch.tensor(cart_lengths, dtype=torch.long)
    cart_mask = create_cart_mask(cart_lengths_tensor, max_cart_len)
    
    # Stack other tensors
    candidate_indices = torch.tensor(
        [s["candidate_index"] for s in batch], dtype=torch.long
    )
    labels = torch.tensor(
        [s["label"] for s in batch], dtype=torch.float32
    ).unsqueeze(1)  # (batch, 1)
    
    user_features = torch.stack([s["user_features"] for s in batch])
    restaurant_features = torch.stack([s["restaurant_features"] for s in batch])
    cart_dynamic_features = torch.stack([s["cart_dynamic_features"] for s in batch])
    context_features = torch.stack([s["context_features"] for s in batch])
    
    return {
        "cart_indices": cart_indices,
        "candidate_indices": candidate_indices,
        "labels": labels,
        "user_features": user_features,
        "restaurant_features": restaurant_features,
        "cart_dynamic_features": cart_dynamic_features,
        "context_features": context_features,
        "cart_mask": cart_mask,
    }


